"""
What is the value returned by each index position?
"""
def main():
    
    fruit = "apple"

    print("What is the output of fruit[:]")
    print(fruit[:])
    
    print("What is the output of fruit[ :3]?")
    print(fruit[ :3])
    
    print("What is the output of fruit[ 3:]?")
    print(fruit[ 3:])
    
    print("What is the output of fruit[: -1]?")
    print(fruit[: -1])
    
    print("What is the output of fruit[0 : 11]?")
    print(fruit[0 : 11])

    
main()


